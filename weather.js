let apiKey = "5d80549916decf4f3e8f9ff9addaba88";
let apiUrl = `https://api.openweathermap.org/data/2.5/weather?appid=${apiKey}&units=metric&q=`;
let searchinp = document.querySelector("#inp");
let searchbtn = document.querySelector(".but");
let weatherIcon = document.getElementsByClassName("img1");

async function checkWeather(city) {
    let response = await fetch(apiUrl + city);

    if (response.status == 404){
        document.querySelector("error").style.display ="block";
        document.querySelector(".weather").style.display ="none";
    }
    else{
        document.querySelector(".error").style.display ="none";
        document.querySelector(".weather").style.display ="block";
    }

    let data = await response.json();

    console.log(data);
    document.querySelector('.city-name').innerHTML = data.name;
    document.querySelector('.temp').innerHTML = Math.round(data.main.temp) + "<sup>âˆ˜</sup>C";
    document.querySelector('.humidity').innerHTML = data.main.humidity + "%";
    document.querySelector('.wind').innerHTML = data.wind.speed + "km/h";

if (data.weather[0].main == "Clear") {
    weatherIcon.src = "clear.png";
}
else if (data.weather[0].main == "Clouds") {
    weatherIcon.src = "clouds.png";
}
else if (data.weather[0].main == "Rain") {
    weatherIcon.src = "rain.png";
}
else if (data.weather[0].main == "Drizzle") {
    weatherIcon.src = "drizzle.png";
}
else if( data.weather[0].main == "Snow"){
    weatherIcon.src ="snow.png";
}
else if (data.weather[0].main == "Mist"){
    weatherIcon.src = "mist.png";
}
}
searchbtn.addEventListener('click', () => {
    checkWeather(searchinp.value);
})